/**
 * Generate webterm/src/fixtures.ts from canonical fixtures/*.json.
 * Run: node scripts/sync-fixtures.mjs  (from repo root)
 */
import fs from 'fs'
import path from 'path'
import { fileURLToPath } from 'url'

const __dirname = path.dirname(fileURLToPath(import.meta.url))
const ROOT = path.resolve(__dirname, '..')
const FIX = path.join(ROOT, 'fixtures')
// Monorepo: webterm/src/fixtures.ts — Docker standalone: may use repo layout above
const OUT = fs.existsSync(path.join(ROOT, 'webterm', 'src'))
  ? path.join(ROOT, 'webterm', 'src', 'fixtures.ts')
  : path.join(ROOT, 'src', 'fixtures.ts')

const FILES = [
  ['cases.json', 'CASES', 'Case[]'],
  ['statutes.json', 'STATUTES', 'Statute[]'],
  ['contracts.json', 'CONTRACTS', 'Contract[]'],
  ['jobs.json', 'JOBS', 'AnalysisJob[]'],
  ['audit_log.json', 'AUDIT_LOG', 'AuditEntry[]'],
  ['workflows.json', 'WORKFLOWS', 'Workflow[]'],
  ['negotiation_guides.json', 'NEGOTIATION_GUIDES', 'NegotiationGuidesMap'],
  ['clause_alternatives.json', 'CLAUSE_ALTERNATIVES', 'ClauseAlternativesMap'],
  ['document_profiles.json', 'DOCUMENT_PROFILES', 'DocumentProfilesMap'],
  ['brief_outlines.json', 'BRIEF_OUTLINES', 'BriefOutlinesMap'],
  ['panel_meta.json', 'PANEL_META', 'PanelMeta'],
]

let body = `// AUTO-GENERATED by scripts/sync-fixtures.mjs — do not edit manually.
// Source of truth: fixtures/*.json
import type {
  Case, Statute, Contract, AnalysisJob, AuditEntry, Workflow,
  NegotiationGuidesMap, ClauseAlternativesMap, DocumentProfilesMap, BriefOutlinesMap, PanelMeta,
} from './mcp/types'

`

for (const [file, exportName, typeName] of FILES) {
  const src = path.join(FIX, file)
  if (!fs.existsSync(src)) {
    console.warn(`skip missing ${file}`)
    continue
  }
  const raw = fs.readFileSync(src, 'utf8').trim()
  JSON.parse(raw)
  body += `export const ${exportName} = ${raw} as ${typeName}\n\n`
}

fs.writeFileSync(OUT, body)
console.log(`Wrote ${OUT}`)
